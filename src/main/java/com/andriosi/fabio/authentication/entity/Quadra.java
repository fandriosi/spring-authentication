package com.andriosi.fabio.authentication.entity;

public class Quadra {
    private String codigoCartografico;
    private int quarteirao;
    private String Quadra;
    private String quadraLote;
    private String codigoClassificao;
    private String fonteAlternativa;
    private int registro;

    public String getCodigoCartografico() {
        return codigoCartografico;
    }

    public void setCodigoCartografico(String codigoCartografico) {
        this.codigoCartografico = codigoCartografico;
    }

    public int getQuarteirao() {
        return quarteirao;
    }

    public void setQuarteirao(int quarteirao) {
        this.quarteirao = quarteirao;
    }

    public String getQuadra() {
        return Quadra;
    }

    public void setQuadra(String quadra) {
        Quadra = quadra;
    }

    public String getQuadraLote() {
        return quadraLote;
    }

    public void setQuadraLote(String quadraLote) {
        this.quadraLote = quadraLote;
    }

    public String getCodigoClassificao() {
        return codigoClassificao;
    }

    public void setCodigoClassificao(String codigoClassificao) {
        this.codigoClassificao = codigoClassificao;
    }

    public String getFonteAlternativa() {
        return fonteAlternativa;
    }

    public void setFonteAlternativa(String fonteAlternativa) {
        this.fonteAlternativa = fonteAlternativa;
    }

    public int getRegistro() {
        return registro;
    }

    public void setRegistro(int registro) {
        this.registro = registro;
    }
}
